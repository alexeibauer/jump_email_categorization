defmodule JumpEmailCategorizationWeb.UserRegistrationController do
  use JumpEmailCategorizationWeb, :controller

  alias JumpEmailCategorization.Accounts
  alias JumpEmailCategorization.Accounts.User
  alias JumpEmailCategorizationWeb.UserAuth

  def new(conn, _params) do
    changeset = Accounts.change_user_registration(%User{})
    render(conn, :new, changeset: changeset)
  end

  def create(conn, %{"user" => user_params}) do
    case Accounts.register_user(user_params) do
      {:ok, user} ->
        conn
        |> put_flash(:info, "Account created successfully!")
        |> UserAuth.log_in_user(user)

      {:error, %Ecto.Changeset{} = changeset} ->
        render(conn, :new, changeset: changeset)
    end
  end
end
